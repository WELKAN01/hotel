    private void txtDNIKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDNIKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()==KeyEvent.VK_ENTER ) {
           
            if (!"".equals(txtDNI.getText())) {
                String DNI=this.txtDNI.getText();
                cUNI = modelCli.buscarDNI(DNI);
                this.txtNombre.setText(cUNI.getNombre()+" "+cUNI.getApellido()); 
                }else{
                JOptionPane.showMessageDialog(this, "porfavor ingrese un DNI");
            }
            }
    }//GEN-LAST:event_txtDNIKeyPressed

    private void txtRUCKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRUCKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()==KeyEvent.VK_ENTER) {
            if (!"".equals(txtRUC.getText())) {
                String RUC=this.txtRUC.getText();
                sUNI = modelSer.buscarServicio(RUC);
                this.txtNomServicio.setText(sUNI.getNom_serv());
                this.txtPrecio.setText(""+sUNI.getPrecio());
                changeid=sUNI.getId();
            }else{
                JOptionPane.showMessageDialog(this, "porfavor ingrese el ruc del servicio");
            }
        }
    }//GEN-LAST:event_txtRUCKeyPressed

    private void INGRESSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_INGRESSActionPerformed
        // TODO add your handling code here:
       if (!"".equals(txtNomServicio.getText()) &&!"".equals(txtPrecio.getText())) {
            String nombre,ruc;
        Double precio;
        nombre=this.txtNomServicio.getText();
        ruc=this.txtRUC.getText();
        precio=Double.parseDouble(this.txtPrecio.getText());
        tableModel=(DefaultTableModel) TBfactura.getModel();
        Item++;
         ArrayList lista = new ArrayList();
         lista.add(Item);
         lista.add(changeid);
         lista.add(nombre);
         lista.add(ruc);
         lista.add(precio);
                    Object[] O = new Object[5];
                    O[0] = lista.get(0);
                    O[1] = lista.get(1);
                    O[2] = lista.get(2);
                    O[3] = lista.get(3);
                    O[4] = lista.get(4);
                    tableModel.addRow(O);
                    TBfactura.setModel(tableModel);
                    TotalPagar();
        vaciarServicio();
        }else{
            JOptionPane.showMessageDialog(this,"LLENA LOS CAMPOS FALTANTES");
        }
        
    }//GEN-LAST:event_INGRESSActionPerformed

