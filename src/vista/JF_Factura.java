    private void DELETEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DELETEActionPerformed
        // TODO add your handling code here:
        tableModel=(DefaultTableModel) this.TBfactura.getModel();
        tableModel.removeRow(TBfactura.getSelectedRow());
        TotalPagar();
        txtRUC.requestFocus();
    }//GEN-LAST:event_DELETEActionPerformed
    
    private void TotalPagar() {
        Totalpagar = 0.00;
        int numFila = TBfactura.getRowCount();
        for (int i = 0; i < numFila; i++) {
            double cal = Double.parseDouble(String.valueOf(TBfactura.getModel().getValueAt(i, 4)));
            Totalpagar = Totalpagar + cal;
        }
        changePRICE.setText(String.format("%.2f", Totalpagar));
    }
    
    public void vaciarServicio(){
        this.txtNomServicio.setText("");
        this.txtRUC.setText("");
        this.txtPrecio.setText("");
        this.txtRUC.requestFocus();
    }
    public void vaciarTodo(){
        this. txtDNI.setText("");
        this.txtNombre.setText("");
        this.txtNomServicio.setText("");
        this.txtRUC.setText("");
        this.txtPrecio.setText("");
        tableModel=(DefaultTableModel) this.TBfactura.getModel();
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            tableModel.removeRow(0);
        }
        this.txtDNI.requestFocus();
    }
    
    public void REGISTRARFACTURA(){
        String Nombre=txtNombre.getText();
        String Empleado=JF_Factura.resultadoEmpleado.getText();
        double Monto=Double.parseDouble(changePRICE.getText());
        String fecha=txtfecha.getText();
        Factura RF= new Factura();
        RF.setNombre_cli(Nombre);
        RF.setNombre_emp(Empleado);
        RF.setPrecio_total(Monto);
        RF.setFecha(fecha);
        modelFD.INSERTVENTA(RF);
    }
    public void REGISTRARDETALLE(){
        int id=modelFD.IdFactura();
        for (int i = 0; i <TBfactura.getModel().getRowCount(); i++) {
            int id_serv=Integer.parseInt(TBfactura.getModel().getValueAt(i,1).toString());
            double precio=Double.parseDouble(TBfactura.getModel().getValueAt(i, 4).toString());
            dUNI.setServicio(id_serv);
            dUNI.setPrecio(precio);
            dUNI.setId_factura(id);
            modelFD.INSERTARDETALLE(dUNI);
        }
    }
    
